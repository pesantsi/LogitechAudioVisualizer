    if (!(layout == "US"))
    {
      if (!(layout == "INTL"))
      {
        if (!(layout == "JPN"))
        {
          if (!(layout == "KOR"))
          {
            if (layout == "RU")
            {
              position = position_RU;
              size = size_RU;
            }
            else
            {
              position = (int[]) null;
              size = (float[]) null;
            }
          }
          else
          {
            position = position_KOR;
            size = size_KOR;
          }
        }
        else
        {
          position = position_JPN;
          size = size_JPN;
        }
      }
      else
      {
        position = position_INTL;
        size = size_INTL;
      }
    }
    else
    {
      position = position_US;
      size = size_US;
    }
  }
